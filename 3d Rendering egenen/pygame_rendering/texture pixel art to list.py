# Imports
import sys
import pygame
#import ctypes

# Increas Dots Per inch so it looks sharper
#ctypes.windll.shcore.SetProcessDpiAwareness(True)

# Variables
TEXTURE_SIZE = 16
dragging = False
# Create a 16x16 list containing [0, 0, 0]
picture = [[[255, 255, 255] for _ in range(TEXTURE_SIZE)] for _ in range(TEXTURE_SIZE)]
# Initial color
drawColor = [0, 0, 0]

# Pygame Configuration
pygame.init()
fps = 300
fpsClock = pygame.time.Clock()
width_screen, height_screen = TEXTURE_SIZE * TEXTURE_SIZE, TEXTURE_SIZE * TEXTURE_SIZE
screen = pygame.display.set_mode((width_screen, height_screen), pygame.RESIZABLE)

font = pygame.font.SysFont('Arial', 20)


#place the block on the grid and updates it
def place_color_block(mouse_x, mouse_y):
    x = int(mouse_x / TEXTURE_SIZE)
    y = int(mouse_y / TEXTURE_SIZE)
    picture[y][x] = drawColor

# Save the surface to the Disk
def save():
    pygame.image.save(screen, "canvas.png")

#asks for 3 intijers for color
def get_three_integers():
    while True:
        user_input = input("Please enter three integers separated by spaces for your color: ")
        try:
            # Split the input and convert to integers
            integers = list(map(int, user_input.split()))
            #checks to see if proper legnth and if not < 256
            if len(integers) == 3 and integers[0] < 256 and integers[1] < 256 and integers[2] < 256:
                return integers
            else:
                print("You must enter exactly three integers.")
        except ValueError:
            print("Invalid input. Please enter integers only.")



#asks user if they want to edit a prevous immage
user_immage_question = input("Enter a prevous list limage if you want. (put 0 if no)")
if user_immage_question == "0":
    print("")
else:
    picture = user_immage_question
#Prints how to do stuff
print("Use esc to save (you can still edit after)")
print("type c to change color")
print("click to place")
# Game loop.
while True:
    screen.fill((255, 255, 255))
    #gets events to do stuff
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            pygame.quit()
            sys.exit()
        #checks if the mouse is pressed down
        if event.type == pygame.MOUSEBUTTONDOWN:
            dragging = True
        if event.type == pygame.MOUSEBUTTONUP:
            dragging = False
        
        if event.type == pygame.KEYDOWN:
            # Print the grid to save
            if event.key == pygame.K_ESCAPE:
                print(picture)
            #changes the color
            if event.key == pygame.K_c:
                drawColor = get_three_integers()
    if dragging:
        #places the block based on x y cords
        x , y = pygame.mouse.get_pos()
        place_color_block(x, y)
    #updates the display based on list of list of list
    for tex_y in range(TEXTURE_SIZE):
        for tex_x in range(TEXTURE_SIZE):
            pygame.draw.rect(screen , picture[tex_y][tex_x], (tex_x * TEXTURE_SIZE, tex_y * TEXTURE_SIZE, width_screen / TEXTURE_SIZE, height_screen / TEXTURE_SIZE))
    pygame.display.flip()
    fpsClock.tick(fps)
